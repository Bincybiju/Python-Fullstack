#       50 and above E
#       45 and above P
#       below 45     F
score=int(input("enter the score of a student : "))
if score>=90:
    print("Grade is A+")
elif score>=85:
    print("Grade is A")
elif score>=80:
    print("Grade is B+")
elif score>=75:
    print("Grade is B")
elif score>=70:
    print("Grade is c+")
elif score>=65:
    print("Grade is c")
elif score>=60:
    print("Grade is D")
elif score>=50:
    print("Grade is E")
elif score>=45:
    print("Grade is P")
else:
    print("Grade is F")
    